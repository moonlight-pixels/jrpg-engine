package com.moonlightpixels.jrpg.graphics;

public final class Coordinate2D {
    private final int x;
    private final int y;

    public Coordinate2D(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
